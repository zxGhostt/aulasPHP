<?php
    //operadores ralacionados como: >, <, >=, <=, ==, != 

    $n1 = 1;
    $n2 = 3;

    $res1 = ($n1 == $n2); #igual
    $res2 = 10 > 20; #maior
    $res3 = 10 < 20; #menor
    $res4 = 10 >= 20; #maior ou igual
    $res5 = 10 <= 20; #menor ou igual
    $res6 = 10 != 20; #diferente


    var_dump($res1);
    var_dump($res2);
    var_dump($res3);
    var_dump($res4);
    var_dump($res5);
    var_dump($res6);
?>