<?php
    
    //operdaor de incremento (++)

    $valor = 10;
    $valor++;//soma mais 1
    $valor += 30;//soma 30 ao valor anterior
    $valor = $valor + 30;  

    echo "".$valor;

    //operdaor de incremento (--)

    
    $valor--;// tira 1
   
    echo "\n".$valor;

    $valor -= 20;
    $valor = $valor - 20;

    echo "\n".$valor;
    
?>